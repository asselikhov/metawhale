/**
 * –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π –ú–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ –≠—Å–∫—Ä–æ—É
 * –ü—Ä–µ–¥–æ—Ç–≤—Ä–∞—â–∞–µ—Ç –∑–∞–º–æ—Ä–æ–∑–∫—É —Å—Ä–µ–¥—Å—Ç–≤ —á–µ—Ä–µ–∑ —Ä–µ–≥—É–ª—è—Ä–Ω—ã–µ –ø—Ä–æ–≤–µ—Ä–∫–∏
 */

require('dotenv').config();

const { connectDatabase, disconnectDatabase } = require('../src/database/models');
const escrowSafetySystem = require('../src/services/escrowSafetySystem');

class EscrowMonitoringService {
  constructor() {
    this.monitoringInterval = 30 * 60 * 1000; // 30 –º–∏–Ω—É—Ç
    this.isRunning = false;
  }

  /**
   * –ó–∞–ø—É—Å–∫ –ø–æ—Å—Ç–æ—è–Ω–Ω–æ–≥–æ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
   */
  async startMonitoring() {
    if (this.isRunning) {
      console.log('üîç Monitoring is already running');
      return;
    }

    this.isRunning = true;
    console.log('üöÄ Starting escrow monitoring service...');

    try {
      await connectDatabase();
      console.log('‚úÖ Connected to database for monitoring');

      // –ü–µ—Ä–≤–æ–Ω–∞—á–∞–ª—å–Ω–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞
      await this.performMonitoringCheck();

      // –ó–∞–ø—É—Å–∫ –ø–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫–∏—Ö –ø—Ä–æ–≤–µ—Ä–æ–∫
      this.intervalId = setInterval(async () => {
        try {
          await this.performMonitoringCheck();
        } catch (error) {
          console.error('‚ùå Monitoring check failed:', error);
        }
      }, this.monitoringInterval);

      console.log(`üîÑ Monitoring started, checking every ${this.monitoringInterval / 60000} minutes`);

    } catch (error) {
      console.error('‚ùå Failed to start monitoring:', error);
      this.isRunning = false;
      throw error;
    }
  }

  /**
   * –û—Å—Ç–∞–Ω–æ–≤–∫–∞ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
   */
  async stopMonitoring() {
    if (!this.isRunning) {
      console.log('üõë Monitoring is not running');
      return;
    }

    this.isRunning = false;
    
    if (this.intervalId) {
      clearInterval(this.intervalId);
      this.intervalId = null;
    }

    await disconnectDatabase();
    console.log('üõë Escrow monitoring stopped');
  }

  /**
   * –í—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –ø—Ä–æ–≤–µ—Ä–∫–∏ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
   */
  async performMonitoringCheck() {
    try {
      console.log('üîç [MONITOR] Starting escrow monitoring check...');
      
      // 1. –ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–∞–≤–∏—Å—à–∏—Ö —ç—Å–∫—Ä–æ—É
      await escrowSafetySystem.checkStuckEscrows();
      
      // 2. –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–æ—Å—Ä–æ—á–µ–Ω–Ω—ã—Ö —Å–¥–µ–ª–æ–∫
      await this.checkExpiredTrades();
      
      // 3. –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–µ—Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–π –±–∞–ª–∞–Ω—Å–æ–≤
      await this.checkBalanceDiscrepancies();
      
      // 4. –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
      await this.generateMonitoringStats();
      
      console.log('‚úÖ [MONITOR] Monitoring check completed successfully');
      
    } catch (error) {
      console.error('‚ùå [MONITOR] Monitoring check failed:', error);
    }
  }

  /**
   * –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–æ—Å—Ä–æ—á–µ–Ω–Ω—ã—Ö —Å–¥–µ–ª–æ–∫
   */
  async checkExpiredTrades() {
    try {
      const { P2PTrade } = require('../src/database/models');
      
      // –ù–∞–π—Ç–∏ —Å–¥–µ–ª–∫–∏ —Å—Ç–∞—Ä—à–µ 2 —á–∞—Å–æ–≤ –≤ —Å—Ç–∞—Ç—É—Å–µ escrow_locked –∏–ª–∏ payment_pending
      const expiredTrades = await P2PTrade.find({
        status: { $in: ['escrow_locked', 'payment_pending'] },
        'timeTracking.createdAt': { $lt: new Date(Date.now() - 2 * 60 * 60 * 1000) }
      }).populate('sellerId buyerId');

      if (expiredTrades.length > 0) {
        console.log(`‚ö†Ô∏è [MONITOR] Found ${expiredTrades.length} expired trades`);
        
        for (const trade of expiredTrades) {
          try {
            // –ü—Ä–æ–≤–µ—Ä–∏—Ç—å, –∏—Å—Ç–µ–∫ –ª–∏ —Ä–µ–∞–ª—å–Ω—ã–π —Ç–∞–π–º–∞—É—Ç
            const expiryTime = trade.timeTracking?.expiresAt || 
                              new Date(trade.timeTracking.createdAt.getTime() + 30 * 60 * 1000);
            
            if (Date.now() > expiryTime.getTime()) {
              console.log(`üïê [MONITOR] Processing expired trade: ${trade._id}`);
              
              const p2pService = require('../src/services/p2p');
              await p2pService.cancelTradeWithTimeout(trade._id);
              
              console.log(`‚úÖ [MONITOR] Processed expired trade: ${trade._id}`);
            }
          } catch (tradeError) {
            console.error(`‚ùå [MONITOR] Failed to process expired trade ${trade._id}:`, tradeError);
          }
        }
      } else {
        console.log('‚úÖ [MONITOR] No expired trades found');
      }
      
    } catch (error) {
      console.error('‚ùå [MONITOR] Error checking expired trades:', error);
    }
  }

  /**
   * –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–µ—Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–π –±–∞–ª–∞–Ω—Å–æ–≤
   */
  async checkBalanceDiscrepancies() {
    try {
      const { User } = require('../src/database/models');
      const walletService = require('../src/services/walletService');
      
      // –ù–∞–π—Ç–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π —Å CES –±–∞–ª–∞–Ω—Å–∞–º–∏ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
      const usersWithBalance = await User.find({
        walletAddress: { $exists: true, $ne: null },
        $or: [
          { cesBalance: { $gt: 0 } },
          { escrowCESBalance: { $gt: 0 } }
        ]
      }).limit(10); // –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ 10 –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –∑–∞ —Ä–∞–∑
      
      let discrepanciesFound = 0;
      
      for (const user of usersWithBalance) {
        try {
          // –ü–æ–ª—É—á–∏—Ç—å —Ä–µ–∞–ª—å–Ω—ã–π –±–∞–ª–∞–Ω—Å —Å –±–ª–æ–∫—á–µ–π–Ω–∞
          const blockchainBalance = await walletService.getCESBalance(user.walletAddress);
          const dbTotalBalance = (user.cesBalance || 0) + (user.escrowCESBalance || 0);
          
          const difference = Math.abs(blockchainBalance - dbTotalBalance);
          
          if (difference > 0.0001) { // –î–æ–ø—É—Å—Ç–∏–º–∞—è –ø–æ–≥—Ä–µ—à–Ω–æ—Å—Ç—å
            console.log(`‚ö†Ô∏è [MONITOR] Balance discrepancy for user ${user.chatId}:`);
            console.log(`   Database: ${dbTotalBalance} CES (${user.cesBalance} available + ${user.escrowCESBalance} escrow)`);
            console.log(`   Blockchain: ${blockchainBalance} CES`);
            console.log(`   Difference: ${difference.toFixed(4)} CES`);
            
            discrepanciesFound++;
            
            // –°–æ–∑–¥–∞—Ç—å –∑–∞–ø–∏—Å—å –¥–ª—è —Ä–∞—Å—Å–ª–µ–¥–æ–≤–∞–Ω–∏—è
            const { EscrowTransaction } = require('../src/database/models');
            const discrepancyRecord = new EscrowTransaction({
              userId: user._id,
              type: 'balance_discrepancy_detected',
              tokenType: 'CES',
              amount: difference,
              status: 'pending',
              reason: `Balance discrepancy: DB=${dbTotalBalance}, Blockchain=${blockchainBalance}`,
              createdAt: new Date()
            });
            
            await discrepancyRecord.save();
          }
          
        } catch (balanceError) {
          console.error(`‚ùå [MONITOR] Error checking balance for user ${user.chatId}:`, balanceError);
        }
      }
      
      if (discrepanciesFound === 0) {
        console.log('‚úÖ [MONITOR] No balance discrepancies found');
      } else {
        console.log(`‚ö†Ô∏è [MONITOR] Found ${discrepanciesFound} balance discrepancies`);
      }
      
    } catch (error) {
      console.error('‚ùå [MONITOR] Error checking balance discrepancies:', error);
    }
  }

  /**
   * –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∏ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
   */
  async generateMonitoringStats() {
    try {
      const { EscrowTransaction, P2PTrade, User } = require('../src/database/models');
      
      // –°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞ —ç—Å–∫—Ä–æ—É —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–π –∑–∞ –ø–æ—Å–ª–µ–¥–Ω–∏–µ 24 —á–∞—Å–∞
      const last24Hours = new Date(Date.now() - 24 * 60 * 60 * 1000);
      
      const stats = {
        activeEscrows: await EscrowTransaction.countDocuments({
          type: 'lock',
          status: 'completed',
          createdAt: { $gte: last24Hours }
        }),
        successfulRefunds: await EscrowTransaction.countDocuments({
          type: 'refund',
          status: 'completed',
          createdAt: { $gte: last24Hours }
        }),
        manualInterventions: await EscrowTransaction.countDocuments({
          type: { $in: ['manual_intervention_required', 'timeout_intervention_required'] },
          status: 'pending',
          createdAt: { $gte: last24Hours }
        }),
        activeTrades: await P2PTrade.countDocuments({
          status: { $in: ['escrow_locked', 'payment_pending'] }
        }),
        usersWithEscrow: await User.countDocuments({
          escrowCESBalance: { $gt: 0 }
        })
      };
      
      console.log('üìä [MONITOR] Escrow monitoring statistics (24h):');
      console.log(`   Active escrows: ${stats.activeEscrows}`);
      console.log(`   Successful refunds: ${stats.successfulRefunds}`);
      console.log(`   Manual interventions needed: ${stats.manualInterventions}`);
      console.log(`   Active trades: ${stats.activeTrades}`);
      console.log(`   Users with escrowed funds: ${stats.usersWithEscrow}`);
      
      if (stats.manualInterventions > 0) {
        console.log('‚ö†Ô∏è [MONITOR] ATTENTION: Manual interventions required!');
      }
      
    } catch (error) {
      console.error('‚ùå [MONITOR] Error generating monitoring stats:', error);
    }
  }

  /**
   * –û–¥–Ω–æ—Ä–∞–∑–æ–≤–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞ (–¥–ª—è —Ä—É—á–Ω–æ–≥–æ –∑–∞–ø—É—Å–∫–∞)
   */
  async runSingleCheck() {
    try {
      console.log('üîç Running single escrow monitoring check...');
      
      await connectDatabase();
      await this.performMonitoringCheck();
      await disconnectDatabase();
      
      console.log('‚úÖ Single monitoring check completed');
      
    } catch (error) {
      console.error('‚ùå Single monitoring check failed:', error);
      await disconnectDatabase();
      throw error;
    }
  }
}

// –ó–∞–ø—É—Å–∫ –µ—Å–ª–∏ —Ñ–∞–π–ª –≤—ã–∑–≤–∞–Ω –Ω–∞–ø—Ä—è–º—É—é
if (require.main === module) {
  const monitor = new EscrowMonitoringService();
  
  // –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–∏–≥–Ω–∞–ª–æ–≤ –¥–ª—è –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–≥–æ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è
  process.on('SIGINT', async () => {
    console.log('\\nüõë Received SIGINT, stopping monitoring...');
    await monitor.stopMonitoring();
    process.exit(0);
  });
  
  process.on('SIGTERM', async () => {
    console.log('\\nüõë Received SIGTERM, stopping monitoring...');
    await monitor.stopMonitoring();
    process.exit(0);
  });
  
  // –ó–∞–ø—É—Å–∫ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
  monitor.startMonitoring()
    .catch((error) => {
      console.error('üí• Failed to start monitoring:', error);
      process.exit(1);
    });
}

module.exports = EscrowMonitoringService;